// At build time, fetch the latest blog indexes from both source sites and emit
// a single typed TypeScript module the BlogHubPage can import. Keeps SEO
// strong (content is baked into the static bundle) and avoids runtime CORS.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_DIR = path.join(ROOT, "src/generated");
const OUT_FILE = path.join(OUT_DIR, "blog-aggregate.ts");

const SOURCES = [
  {
    key: "syncosystem",
    url: process.env.SYNCOSYSTEM_BLOG_INDEX_URL || "https://syncosystem.com/blog-index.json",
    label: "SyncoSystem",
  },
  {
    key: "precision-health",
    url: process.env.PRECISION_BLOG_INDEX_URL || "https://precisionhealthandweightloss.com/blog-index.json",
    label: "Precision Health",
  },
];

function isFilePath(urlOrPath) {
  return (
    !urlOrPath.startsWith("http://") &&
    !urlOrPath.startsWith("https://") &&
    (urlOrPath.startsWith("file://") ||
      fs.existsSync(urlOrPath) ||
      /^[A-Za-z]:[\\/]/.test(urlOrPath))
  );
}

async function loadIndex(source) {
  const src = source.url;
  try {
    let data;
    if (isFilePath(src)) {
      const p = src.startsWith("file://") ? fileURLToPath(src) : src;
      data = JSON.parse(fs.readFileSync(p, "utf8"));
      console.log(`[blog-aggregate] ${source.key}: read ${p}`);
    } else {
      const res = await fetch(src, { headers: { Accept: "application/json" } });
      if (!res.ok) {
        console.warn(`[blog-aggregate] ${source.key} HTTP ${res.status} at ${src}`);
        return { ok: false, articles: [] };
      }
      data = await res.json();
    }
    const articles = Array.isArray(data?.articles) ? data.articles : [];
    console.log(`[blog-aggregate] ${source.key}: ${articles.length} articles`);
    return { ok: true, articles, generatedAt: data?.generatedAt ?? null };
  } catch (err) {
    console.warn(`[blog-aggregate] ${source.key} load failed:`, err?.message ?? err);
    return { ok: false, articles: [] };
  }
}

function normalize(article, source) {
  return {
    slug: String(article.slug ?? ""),
    title: String(article.title ?? ""),
    author: article.author ? String(article.author) : null,
    date: article.date ? String(article.date) : null,
    category: article.category ? String(article.category) : null,
    excerpt: article.excerpt ? String(article.excerpt) : "",
    readTime: article.readTime ? String(article.readTime) : null,
    heroImage: article.heroImage ? String(article.heroImage) : null,
    canonicalUrl: String(article.canonicalUrl ?? ""),
    source,
  };
}

async function main() {
  const results = await Promise.all(SOURCES.map(loadIndex));
  const [synco, precision] = results;

  const syncoArticles = synco.articles.map((a) => normalize(a, "syncosystem"));
  const precisionArticles = precision.articles.map((a) => normalize(a, "precision-health"));

  const sortByDateDesc = (a, b) => {
    const da = a.date ? new Date(a.date).getTime() : 0;
    const db = b.date ? new Date(b.date).getTime() : 0;
    return db - da;
  };
  syncoArticles.sort(sortByDateDesc);
  precisionArticles.sort(sortByDateDesc);

  const payload = {
    generatedAt: new Date().toISOString(),
    sources: {
      syncosystem: { count: syncoArticles.length, fetched: synco.ok, articles: syncoArticles },
      "precision-health": {
        count: precisionArticles.length,
        fetched: precision.ok,
        articles: precisionArticles,
      },
    },
  };

  const ts = `// AUTO-GENERATED by scripts/fetch-blog-aggregate.mjs — do not edit.
// Fetched from syncosystem.com and precisionhealthandweightloss.com blog indexes.

export interface AggregatedArticle {
  slug: string;
  title: string;
  author: string | null;
  date: string | null;
  category: string | null;
  excerpt: string;
  readTime: string | null;
  heroImage: string | null;
  canonicalUrl: string;
  source: "syncosystem" | "precision-health";
}

export interface AggregatedSource {
  count: number;
  fetched: boolean;
  articles: AggregatedArticle[];
}

export interface BlogAggregate {
  generatedAt: string;
  sources: {
    syncosystem: AggregatedSource;
    "precision-health": AggregatedSource;
  };
}

export const blogAggregate: BlogAggregate = ${JSON.stringify(payload, null, 2)};
`;

  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.writeFileSync(OUT_FILE, ts);
  console.log(
    `[blog-aggregate] wrote ${syncoArticles.length + precisionArticles.length} total articles to ${OUT_FILE}`
  );

  if (!synco.ok && !precision.ok) {
    console.warn("[blog-aggregate] WARNING: neither source returned data — hub will render empty sections");
  }
}

main();
